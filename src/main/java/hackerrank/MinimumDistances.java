package hackerrank;

import java.util.Scanner;

/**
 * https://www.hackerrank.com/challenges/minimum-distances
 */
public class MinimumDistances {
    public static void main(String[] args) {
        Scanner in = new Scanner(System.in);
        int n = in.nextInt();
        int A[] = new int[n];
        for (int A_i = 0; A_i < n; A_i++) {
            A[A_i] = in.nextInt();
        }
    }
}
